package homework.lesson4.less.task3;

public class Document {
    private String name;
    private int pagecount;

    public Document(String name, int pagecount) {
        this.name = name;
        this.pagecount = pagecount;
    }

    public String getName() {
        return name;
    }

    public int getPagecount() {
        return pagecount;
    }
}
